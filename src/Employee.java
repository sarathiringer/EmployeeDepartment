
public class Employee {
	
	private String empID;
	private int salary;
	
	public String getEmpID() {
		return empID;
		
	}
	public void setEmpID(String empID) {
		this.empID = empID;
		
	}
	public int getSalary() {
		return salary;
	}
	
	public void setSalary(int salary) {
		this.salary = salary;

	}

}
